package cc.model;

public class School {
	private String school_id;
	private String school_name;

	public String getSchoolId() {
		return school_id;
	}

	public void setSchoolId(String Id) {
		school_id=Id;
	}

	public String getSchoolName() {
		return school_name;
	}

	public void setSchoolName(String name) {
		school_name = name;
	} 
}